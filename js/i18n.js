function empiricaTranslate(lang, path) {
  return path.split('.').reduce((acc, key) => (acc && acc[key] !== undefined) ? acc[key] : undefined, translations[lang]);
}

function empiricaApplyLanguage(lang) {
  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const val = empiricaTranslate(lang, el.dataset.i18n);
    if (val !== undefined) el.textContent = val;
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const val = empiricaTranslate(lang, el.dataset.i18nPlaceholder);
    if (val !== undefined) el.setAttribute('placeholder', val);
  });

  const titleKey = document.body.dataset.i18nTitle;
  if (titleKey) {
    const val = empiricaTranslate(lang, titleKey);
    if (val !== undefined) document.title = val;
  }

  document.querySelectorAll('.lang-switch button').forEach((btn) => {
    btn.setAttribute('aria-current', btn.dataset.lang === lang ? 'true' : 'false');
  });

  const sessionLangSelect = document.querySelector('#language');
  if (sessionLangSelect) sessionLangSelect.value = lang;

  localStorage.setItem('empirica_lang', lang);
}

function empiricaInitLanguage() {
  const saved = localStorage.getItem('empirica_lang');
  const browserLang = (navigator.language || 'pl').slice(0, 2);
  const lang = saved || (browserLang === 'en' ? 'en' : 'pl');
  empiricaApplyLanguage(lang);

  document.querySelectorAll('.lang-switch button').forEach((btn) => {
    btn.addEventListener('click', () => empiricaApplyLanguage(btn.dataset.lang));
  });
}

document.addEventListener('DOMContentLoaded', empiricaInitLanguage);
